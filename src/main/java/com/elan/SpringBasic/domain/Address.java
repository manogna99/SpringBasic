package com.elan.SpringBasic.domain;

public class Address {

	
private	String state;
private	int addid;
private	String city;
public String getState() {
	return state;
}
public void setState(String state) {
	this.state = state;
}
public int getPno() {
	return addid;
}
public void setPno(int addid) {
	this.addid = addid;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
	
	
}
